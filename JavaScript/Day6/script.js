// 1. for
        for(var i=1;i<=5;i++){
            console.log(i);      
        }
// 2. while
        var i=parseInt(prompt("Enter a number"));
        while(i<=25){
            console.log(i);
            i++;
        }  
// 3. do...while
        var j = parseInt(prompt("Enter a number"));       
        do {
            console.log(j);
            j = j + 2;
        } 
        while (j <= 20);